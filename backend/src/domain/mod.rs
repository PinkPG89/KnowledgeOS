//! # Domain (도메인) 모듈
//!
//! 이 모듈은 애플리케이션의 핵심 비즈니스 규칙과 핵심 엔티티/값 객체(Value Object)들을 정의합니다.
//!
//! ## 도메인 주도 설계(Domain-Driven Design, DDD)와 격리 원칙
//! * **독립성 유지**: `domain` 모듈 내부의 코드는 HTTP 웹 프레임워크(`Axum`), 특정 데이터베이스, 또는 실제 파일 시스템(I/O) 등의 기술적 구현 세부사항에 절대 의존하지 않습니다.
//! * **비즈니스 규칙의 보호**: 예를 들어 "경로는 항상 상대 경로여야 하고 상위 디렉토리로 탈출할 수 없다"는 핵심 보안/비즈니스 제약 조건을 프레임워크나 저장소 계층과 완전히 무관하게 이 모듈에서 강제합니다.
//! * **뉴타입 패턴(Newtype Pattern)**: 일반적인 String 타입을 사용하는 대신, 특정한 유효성 검증 단계를 통과했음을 타입 시스템 수준에서 보장하는 특수 타입을 정의하여 잘못된 값이 소스코드 전반에 유입되는 것을 원천 차단합니다.

